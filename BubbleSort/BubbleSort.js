// If the number at position "i" is larger than the following
// number in the List, then swap the two values by index.
function swap(numbers, i) {
  var left = numbers[i];
  var right = numbers[i+1];
  numbers[i] = right;
  numbers[i+1] = left;
}

/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////

// Place the "ith" largest number in the correct position.
function bubble(numbers) {
  for (var i=0; i<numbers.length-1; i++) {
    if (numbers[i] > numbers[i+1]) {
      swap(numbers, i);
    }
  }
}

/////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////

// Sort all of the numbers in the list in ascending order.
function bubbleSort(numbers) {
  for (var i=0; i<numbers.length-1; i++) {
    bubble(numbers);
  }
}

