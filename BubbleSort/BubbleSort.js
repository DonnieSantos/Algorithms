// Alert (to the user) every number in the given list.
function printList(numbers) {
	console.log("Printing sorted list of size " + numbers.length + ".");
  for (var i=0; i<numbers.length; i++) {
    console.log("Number " + numbers[i] + " at position " + i + ".");
  }
}

